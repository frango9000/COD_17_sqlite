    private void refreshTable() {
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTableEditoriales.getModel();
        model.setRowCount(0);
        biblioSQL.queryEditoriales().forEach((in, st) -> {
            Object[] row = {in, st};
            model.addRow(row);
        });
    }

